package vn.philong.game.pikachunew.dc;

import android.os.Bundle;

/**
 * @author Van Hoang Phuong
 * 
 */
public class Params
{
	private Method mMethod;
	private Bundle mInputBundle;
	private Object mInputData;

	public Params()
	{
	}
	
	public Params(Method method)
	{
		mMethod = method;		
	}

	public Params(Method method, Bundle bundle)
	{
		mMethod = method;
		mInputBundle = bundle;
	}

	public Method getMethod()
	{
		return mMethod;
	}

	public Params setMethod(Method method)
	{
		mMethod = method;
		return this;
	}

	public Bundle getInputBundle()
	{
		return mInputBundle;
	}

	public Params setInputBundle(Bundle inputBundle)
	{
		mInputBundle = inputBundle;
		return this;
	}

	public Object getInputData()
    {
	    return mInputData;
    }

	public Params setInputData(Object inputData)
    {
	    mInputData = inputData;
	    return this;
    }
}
